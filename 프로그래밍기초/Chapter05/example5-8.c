#include <stdio.h>

int main(void)
{
	//~a + 1 = -a : 1의 보수를 계산하는 공식!

	int x = 127;		//인트형은 4바이트.. 즉 32비트!

	printf("%5d %08x\n", x, x);		//__127 01111111->0000007f
	printf("%08x\n", x & 1);		//00000001  -> 00000001
	printf("%08x\n", x | 1);		//01111111  -> 0000007f
	printf("%08x\n", x ^ 1);		//01111110  -> 0000007e
	printf("%08x\n", ~(-1));		//00000000  -> 00000000
	printf("%08x\n", ~1);			//11111110  -> fffffffe

	return 0;
}